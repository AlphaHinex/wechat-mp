let randomWord = function (array) {
    return array[Math.floor(Math.random() * array.length)];
};

let formalBonus = function (key, word) {
    return (
        Math.abs(
            Math.sin((key + word).hashCode() + Date.now() * Math.random())
            * Math.random())
        * baseByDays(key)
    ).toFixed(2);
};

